package com.modul2.latihan;

/**
 *
 * @author Windows 10
 */
public class Aithmetika {

    public static void main(String[] args) {
        System.out.println("OPERASI ARITMETIKA " + " PADA TIPE INTEGER");
        int a = 2 + 1;
        int b = a - 1;
        int c = a * b;
        int d = c / 3;
        int e = -a;

        System.out.println("Nilai a = " + a);
        System.out.println("Nilai b = " + b);
        System.out.println("Nilai c = " + c);
        System.out.println("Nilai d = " + d);
        System.out.println("Nilai e = " + e);

        System.out.println("OPERASI ARITMETIKA " + " PADA TIPE FLOATING - POINT");
        double fa = 2 + 1;
        double fb = fa - 1;
        double fc = fa * fb;
        double fd = fc / 3;
        double fe = -fa;

        System.out.println("Nilai a = " + fa);
        System.out.println("Nilai b = " + fb);
        System.out.println("Nilai c = " + fc);
        System.out.println("Nilai d = " + fd);
        System.out.println("Nilai e = " + fe);

    }
}
